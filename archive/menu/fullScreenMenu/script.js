function openFullscreenMenu() {
  var fullscreenMenu = document.getElementById("fullscreen-menu");
  fullscreenMenu.style.display = "block";
}

function closeFullscreenMenu() {
  var fullscreenMenu = document.getElementById("fullscreen-menu");
  fullscreenMenu.style.display = "none";
}



